# Daily digest (kept for future use)
# ─────────────────────────────────────────────────────────────────────────────

def send_daily_digest(cold_targets: list, recruiters: list) -> None:
    today = datetime.now().strftime("%A, %B %-d")
    title = f"📨 Daily digest — {today} · Cold targets + recruiter DMs"
    body  = _build_digest_md(cold_targets, recruiters, today)
    _create_issue(title, body, ["daily-digest"])
    print("[Alert] Daily digest issued")


def _build_digest_md(cold_targets: list, recruiters: list, today: str) -> str:
    lines = [f"# Your outreach targets for {today}\n"]

    lines.append("## 📨 Top 5 Companies — Cold Email Today\n")
    for i, t in enumerate(cold_targets[:5], 1):
        url_str = f" · [View →]({t['url']})" if t.get("url") else ""
        lines.append(f"**{i}. {t['name']}**{url_str}  \n{t['reason']}\n")

    lines.append("\n## 💬 Top 3 Recruiters — DM on LinkedIn\n")
    for i, r in enumerate(recruiters[:3], 1):
        li_str = f" · [LinkedIn]({r['linkedin']})" if r.get("linkedin") else ""
        lines.append(
            f"**{i}. {r['name']}** — {r['title']}{li_str}  \n{r['note']}\n"
        )

    lines.append(
        f"\n---\n*Daily digest · {today} · Job Alert Agent*"
    )
    return "\n".join(lines)
